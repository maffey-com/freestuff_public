<h1>Terms And Conditions</h1>

1. <b>Genuine Free</b>
    It is our intention for every listing on freestuff.co.nz to be genuinely free, with no strings attached.
    
    The following listings are explicitly forbidden as they are not genuinely free:
        i. 2 for 1 offers (or 3 for 1 etc)
        ii. Buy something get something else free
        iii. Discounts of any type
        iv. Digital downloads of any sort.  You must be giving away a genuine physical good or service.
        v. Free quotes.
    
   Transfer of goods.
        <b>The receiver must be able to collect the goods at no cost to the receiver.</b>
        It is acceptable to expect the receiver to have to extract the goods. This must be clearly stated in the listing.
            For example, if you are giving away parts of a house, such as windows or bricks, the giver can insist the receiver bring appropriate tools to remove the items.
        
		The giver may offer to post or deliver the item and it is acceptable for the receiver to pay for this.  This should be clearly stated in the listing.
    
    
2. <b>Banned listing types</b>
    The following items are explicitly banned from the site:
        a.  Any illegal or illicit goods or services, including drugs, offensive weapons etc.
        b.  Any goods or services requiring a licence, including firearms, medicine, alcohol etc.
        c.  Pirated material including movies, software, music, books.
        d.  Adult or x-rated material of any sort.
        e.  Opened food, vitamin or makeup products.
        f.  Anything else we deem offensive.
        
    Freestuff reserves the right to ban any items we deem inappropriate.
        
        